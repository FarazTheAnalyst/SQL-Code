-- ABS function return modulas of numeric values it returns positive value always
SELECT ABS(-10);

-- ceiling function return upper numeric value
SELECT CEILING(49.54);

--FLOOR function return lower value
SELECT FLOOR(49.12);

--SIGN function return 1 for positive value, -1 for negative value, 0 for 0
SELECT SIGN(10), SIGN(-15), SIGN(0);

-- square function square root funtion etc
SELECT SQUARE(5), SQRT(25), PI(), COS(30), SIN(90), TAN(45);

-- exponential function e^2
SELECT EXP(2);